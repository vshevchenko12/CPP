#include <iostream>               // header file library for input and output stream
using namespace std;              /* using the standard namespace to avoid prefixing std::    before standard functions and objects (std::cout, std::cin, etc.)*/

int main() {                      // main function - entry point of the program
  cout << "Hello World!";         // (see-out) output statement to print "Hello World!" to the console
  cout << "Hello World!" << "\n\n";  // two \n characters are used to create a new line after the output
  cout << "I am learning C++" << endl;  // output statement to print "I am learning C++" followed by a new line
  return 0;                       // ends the main function and returns 0 to indicate successful execution
}
