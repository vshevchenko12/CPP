#include <iostream>
using namespace std;

int main() {
  int num1 = 10;               // integer (whole number)
  int num2 = 5;                // integer (whole number)

  cout << "Equal to: " << (num1 == num2) << endl;          // equal to
  cout << "Not equal to: " << (num1 != num2) << endl;      // not equal to
  cout << "Greater than: " << (num1 > num2) << endl;       // greater than
  cout << "Less than: " << (num1 < num2) << endl;          // less than
  cout << "Greater than or equal to: " << (num1 >= num2) << endl; // greater than or equal to
  cout << "Less than or equal to: " << (num1 <= num2) << endl;    // less than or equal to

  return 0;
}