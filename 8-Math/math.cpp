#include <iostream>  // https://www.w3schools.com/cpp/cpp_ref_iostream.asp
#include <cmath>  // https://www.w3schools.com/cpp/cpp_ref_math.asp
using namespace std;

int main() {
  cout << max(5, 10) << endl;  // Output: 10
  cout << min(5, 10) << endl;  // Output: 5
  cout << sqrt(16) << endl;  // Output: 4
  cout << pow(2, 3) << endl;  // Output: 8
  cout << round(3.14) << endl;  // Output: 3
  cout << log(100) << endl;  // Output: 4.60517
  return 0;
}